#!/usr/bin/ruby
#String concatenation can be done in ruby using <<,+ or a blank space 
puts "String concatenation in ruby using <<"
s="Hello"<<"World"
puts s 
puts "String concatenation in ruby using +"
s="Hello" + "World"
puts s 
puts "String concatenation in ruby using a blank space"
s="Hello"  "World"
puts s 

